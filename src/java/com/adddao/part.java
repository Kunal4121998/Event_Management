public class part {
    private int id;
    private String evname,evtype,evhead,evvenue,evtime;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEvname() {
        return evname;
    }

    public void setEvname(String evname) {
        this.evname = evname;
    }

    public String getEvtype() {
        return evtype;
    }

    public void setEvtype(String evtype) {
        this.evtype = evtype;
    }

    public String getEvhead() {
        return evhead;
    }

    public void setEvhead(String evhead) {
        this.evhead = evhead;
    }

    public String getEvvenue() {
        return evvenue;
    }

    public void setEvvenue(String evvenue) {
        this.evvenue = evvenue;
    }

    public String getEvtime() {
        return evtime;
    }

    public void setEvtime(String evtime) {
        this.evtime = evtime;
    }
}
